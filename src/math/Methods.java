package math;

public class Methods {
    public static void main(String[] args) {
        System.out.println("abs : " + Math.abs(-34.5));
        System.out.println("ceil : " + Math.ceil(2.17));
        System.out.println("floor : " + Math.floor(3.14));
        System.out.println("min : " + Math.min(3, -44));
        System.out.println("pow : " + Math.pow(2.71, 3.14));
        System.out.println("sqrt : " + Math.sqrt(225));
    }
}
